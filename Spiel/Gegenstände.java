public class Gegenstände
{
    private int gewicht;
    private int größe;
    private String name;
    
    public Gegenstände()
    {
       
    }
    
    public int getGewicht(){
        return gewicht;
    }
    
    public void setGewicht(int neuGewicht){
        gewicht=neuGewicht;
    }
    
    public String getName(){
        return name;
    }
    
    public void setName(String neuName){
        name=neuName;
    }
    
    public int getGröße(){
        return gewicht;
    }
    
    public void setGröße(int neuGröße){
        größe=neuGröße;
    }
}
